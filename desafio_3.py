# DESAFIO 03 - Crie um algoritmo que leia um número e mostre o seu dobro, triplo e eleve ao quadrado.
numero_usuario = int(input("Digite um valor: "))

dobro = numero_usuario * 2
triplo = numero_usuario * 3
quadrado = numero_usuario ** 2

print(f"O dobro de {numero_usuario} é: {dobro}")
print(f"O triplo de {numero_usuario} é: {triplo}")
print(f"{numero_usuario} elevado ao quadrado é: {quadrado}")