# Fatiamento de Strings
texto = "Curso de Python"
nome = "Augusto Lourenço"

# Capturando o sétimo caractér
print(texto[6])

# Capturando o oitavo caractér
print(nome[7])

# Capturando uma secção de caractéres
print(texto[9:15])
print(texto[9:])

# Capturando o sobrenome
print(nome[8:16])
print(nome[8:])

# Capturando o nome
print(nome[0:7])
print(nome[:7])

# Pulando caractéres
print(texto[9:15:2])
print(texto[9::2])